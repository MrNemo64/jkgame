package me.nemo_64.sdp.utilities;

public final class AsciiTable {

    /**
     * Enquiry
     */
    public static final char ENQ = 5;
    /**
     * Acknowledge
     */
    public static final char ACK = 6;
    /**
     * Not acknowledge
     */
    public static final char NACK = 21;
    /**
     * Start of text
     */
    public static final char STX = 2;
    /**
     * End of text
     */
    public static final char ETX = 3;
    /**
     * End of transmission
     */
    public static final char EOT = 4;

}
